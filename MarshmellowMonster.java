public class MarshmellowMonster {
    private int arms;
    private int eyes;
    private int buttons;
    private String name;

    public MarshmellowMonster() {
    arms = 3;
    eyes = 4;
    buttons = 5;
    name = "Freeloaders";
    }

    public int getArms() {
    return arms;
    }

    public void setArms(int a) {
    arms = a;
    }

    public int getEyes() {
    return eyes;
    }

    public void setEyes(int e) {
    eyes = e;
    }

    public int getButtons() {
    return buttons;
    }

    public void setButtons(int b) {
    buttons = b;
    }

    public String getName() {
    return name;
    }

    public void setName(String n) {
    name = n;
    }
}
